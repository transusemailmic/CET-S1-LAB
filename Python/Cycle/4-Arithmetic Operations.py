# Program to perform arithmetic operations

a=int(input("Enter number 1: "))
b=int(input("Enter number 2: "))
print("Sum of %d and %d is %d" %(a,b,a+b))
print("Difference of %d and %d is %d" %(a,b,a-b))
print("Product of %d and %d is %d" %(a,b,a*b))
print("%d divided by %d is %f" %(a,b,a/b))
print("%d modulo divided by %d is %d" %(a,b,a//b))
print("Modulo of %d and %d is %d" %(a,b,a%b))
print("%d raised to %d is %d" %(a,b,a**b))